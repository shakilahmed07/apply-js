function inchToFeet(_inch) {
  const feet = _inch / 12;
  return feet;
}
// My Feet
let myFeet = inchToFeet(62);
console.log(myFeet);

// My Brother Feet
let brotheFeet = inchToFeet(58);
console.log(brotheFeet);

// My Father Feet
let fatherFeet = inchToFeet(75);
console.log(fatherFeet);

// Mother Feet
let motherFeet = inchToFeet(55);
console.log(motherFeet);
